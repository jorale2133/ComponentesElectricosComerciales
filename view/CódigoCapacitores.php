                Los condensadores o capacitores son elementos eléctricos pasivos, 
                que son capaces de almacenar energía en forma de campo eléctrico. 
                En el mercado se venden distintos tipos de condensadores en los 
                que destacan: los electrolitos, los de poliéster, los cerámicos. 
                <br><br>     
                Los condensadores o capacitores tienen una variedad de códigos y 
                marcas que brindan parámetros sobre el: valor, tolerancia, voltaje
                máximo, etc. Para los capacitores electrolictos vienen marcado el 
                valor explicitamente, para los capacitores ceramicos se puede 
                encontrar la siguiente notación:
                <br><br>  
                En este código, los dos primeros dígitos proporcionan el valor de 
                la capacitancia, mientras que el tercero proporciona el multiplicador 
                o número de ceros que debe añadirse para obtener la capacitancia, 
                las unidades de este valor está en picoFaradios [pF]. Además este 
                puede tener inmediatamente después una letra que expresa la 
                tolerancia. Y al principio de este también puede tener un símbolo 
                que indica el voltaje máximo que soporta el dispositivo.
                <br><br>
                En la parte posterior se encuentra una calculadora para obtener 
                el código de este capacitor.
